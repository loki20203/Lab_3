public class Маршрутизатор : Компютер, IConnectable
{
    public int КількістьПортів { get; private set; }

    public Маршрутизатор(string ipАдреса, int потужність, string типОс, int кількістьПортів)
        : base(ipАдреса, потужність, типОс)
    {
        КількістьПортів = кількістьПортів;
    }

    public void Зєднатися(Компютер іншийКомпютер)
    {
        Console.WriteLine($"Маршрутизатор {IpАдреса} з'єднався з {іншийКомпютер.IpАдреса}");
    }

    public void Відєднатися(Компютер іншийКомпютер)
    {
        Console.WriteLine($"Маршрутизатор {IpАдреса} від'єднався від {іншийКомпютер.IpАдреса}");
    }

    public void ПередатиДані(Компютер іншийКомпютер, string дані)
    {
        Console.WriteLine($"Маршрутизатор {IpАдреса} передав дані '{дані}' до {іншийКомпютер.IpАдреса}");
    }
}
